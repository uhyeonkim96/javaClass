package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; //-128~127
        short s = 32767; //-32768 ~ 32767
        int i = 2147483647; // -2147483648 ~ 2147483647(약 20억)

        // 20억 넘어가면 long이라고 생각하기
        long l;

        // 실수
        float f = 10.0f;
        double d = 10.0;
    }
}
